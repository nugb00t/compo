//=====================================================================================================================

template <class T>
class Holder {
public:
	static void set(T& t);
	static T& get();

private:
	static T* holder();
};

//=====================================================================================================================

template <class T>
void Holder<T>::set(T& t) { 
	t() = &t; 
}

//---------------------------------------------------------------------------------------------------------------------

template <class T>
T& Holder<T>::get() { 
	return *t(); 
}

//---------------------------------------------------------------------------------------------------------------------

template <class T>
T* Holder<T>::holder() {
	static T* t;
	return t;
}


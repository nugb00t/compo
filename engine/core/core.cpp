///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Core::run() {
	Profiler::inst();

	for (std::list<Callable*>::iterator it = systems_.begin(); it != systems_.end(); ++it) {
		threads_.create_thread(boost::ref(**it));
	}

	threads_.join_all();
}


void DynamicMesh::draw(const Matrix44& transform) {
    assert(effect_ && vertexCapacity_ && indexCapacity_);

    effect_->setTransform(transform);
    effect_->begin();

    streamBuffers(vertexCount_, indexCount_ / 3);

    effect_->end();
}


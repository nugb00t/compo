#include "stdafx.h"

#ifdef VIDEO_DIRECT3D9
#include "texture_d3d9.h"

#include "engine.h"

using namespace engine;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

TextureD3D9::TextureD3D9(const TCHAR* const path)
: texture_(NULL), path_(path) {
	assert(path);

	CHECKED_D3D_CALL(D3DXCreateTextureFromFile(&g_engine.videoD3D9->device(), path, &texture_));
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

TextureD3D9::~TextureD3D9() {
	if (texture_)
		texture_->Release();
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void TextureD3D9::activate(const uint stage) {
	assert(texture_);

	CHECKED_D3D_CALL(g_engine.videoD3D9->device().SetTexture(stage, texture_));
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#endif
